package com.kabilan;

public class Main {
    public static void main(String[] args) {

        /*
        syntax for array:
            datatype[] arr_name/variable_name  = new datatype[size];
         */

        // integer array
        int[] rno = new int[5];
        // or
        int[] rno1 = {0, 4, 4, 5, 3};
        System.out.println(rno1[3]);

        ///string array

        String[] arr= new String[5];
        System.out.println(arr[4]);
    }
}